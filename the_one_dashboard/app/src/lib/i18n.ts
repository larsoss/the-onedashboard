/**
 * Lightweight i18n for The-One Dashboard.
 *
 * Locale is detected from:
 *  1. HA config language  (set via setLocale() after /api/config loads)
 *  2. navigator.language  (sync fallback used for first render)
 *  3. 'en'               (ultimate fallback)
 *
 * Usage:  t('key')            – plain string
 *         t('key', { n: 3 }) – with variable substitution ({n} → "3")
 */

// ── Locale state ─────────────────────────────────────────────────────────────

let _locale = resolveLocale(
  typeof navigator !== 'undefined' ? navigator.language : 'en'
)

function resolveLocale(raw: string): string {
  const base = raw.split('-')[0].toLowerCase()
  // Map Norwegian variants to 'no'
  if (base === 'nb' || base === 'nn') return 'no'
  return base
}

export function setLocale(lang: string) {
  _locale = resolveLocale(lang)
}

export function getLocale(): string {
  return _locale
}

