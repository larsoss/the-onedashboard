const TRANSLATIONS: Record<string, Partial_EN> = {
  en: EN, nl: NL, de: DE, fr: FR, es: ES, it: IT, pt: PT, pl: PL, sv: SV, da: DA, no: NO, fi: FI,
}

// ── t() function ──────────────────────────────────────────────────────────────

export function t(key: TranslationKey, vars?: Record<string, string | number>): string {
  const lang = TRANSLATIONS[_locale] ?? EN
  let str = (lang as typeof EN)[key] ?? EN[key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.split(`{${k}}`).join(String(v))
    }
  }
  return str
}

/** Pluralise helper: n=1 → 'one' key, else 'many' key */
export function tn(n: number, oneKey: TranslationKey, manyKey: TranslationKey): string {
  return t(n === 1 ? oneKey : manyKey, { n })
}
